      <h1>Résolution des problèmes d'affichage</h1>
      <p>Lorsque vous utilisez la nouvelle interface "EZCast Podcast Manager" pour la première fois, il se peut que vous ayez des problèmes d'affichage.</p>
      <p>Ces problèmes devraient apparaître sous la forme suivante : <br/></p>
      <p>&nbsp;</p>
      <img src="./images/help/display_issue_1.jpg" width="650px"/><br/>
      <p>&nbsp;</p>
      <img src="./images/help/display_issue_2.jpg" /><br/>
      <p>&nbsp;</p>
      <p>Pour résoudre ce problème, il suffit de vider le cache de votre navigateur web et de rafraîchir la page. La nouvelle version de l'interface devrait alors s'afficher correctement.</p>
      <p>Voici comment procéder: <br/><br/>
          <strong>Google Chrome</strong><br/>
           Il suffit de se rendre à cette URL : <strong>chrome://settings/clearBrowserData</strong> ou par le menu > Paramètres > Historique > Effacer les données de navigation : Sélectionnez la période, le contenu voulu puis validez.<br/><br/>
           <strong>Mozilla Firefox</strong><br/>
           Allez dans le menu > Outils > Supprimer l'historique récent > Tout cocher > Valider <br/><br/>
           <strong>Internet Explorer</strong><br/>
           Allez dans le menu > Outils > Options internet > Onglet "Général" > Supprimer > En bas, cocher la case "Tout supprimer" > Valider <br/><br/>
           <strong>Safari</strong><br/>
           Allez dans le menu Safari > Vider le cache ou CMD + ALT + E <br/><br/>
           <strong>Mozilla Firefox</strong><br/>
           Allez dans le menu > Outils > Préférences > Onglet "Avancé" > Rubrique "Historique" > Cliquer sur "Vider maintenant" correspondant à "Cache disque" <br/><br/></p>
     
